document.addEventListener("click", (event) => {
  const viewButton = event.target.closest("[data-view]");
  const close = event.target.closest("[data-close]");

  if (viewButton) showView(viewButton.dataset.view);
  if (close) closeModal();
});

focusOpen.addEventListener("click", () => {
  openProject(focusedProject);
});

prevButton.addEventListener("click", () => {
  if (activeProject > 0) openProject(activeProject - 1);
});

nextButton.addEventListener("click", () => {
  if (activeProject < projects.length - 1) openProject(activeProject + 1);
});

document.addEventListener("keydown", (event) => {
  if (!modal.classList.contains("is-open")) return;
  if (event.key === "Escape") closeModal();
  if (event.key === "ArrowLeft" && activeProject > 0) openProject(activeProject - 1);
  if (event.key === "ArrowRight" && activeProject < projects.length - 1) openProject(activeProject + 1);
});

window.addEventListener("resize", () => {
  const bounds = viewportBounds();
  bodies.forEach((body) => {
    const size = body.token.offsetWidth || 116;
    const bleed = edgeBleed(size);
    body.x = Math.min(Math.max(-bleed, body.x), bounds.width - size + bleed);
    body.y = Math.min(Math.max(-bleed, body.y), bounds.height - size + bleed);
  });
});

